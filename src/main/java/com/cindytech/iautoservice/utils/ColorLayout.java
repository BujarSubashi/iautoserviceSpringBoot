package com.cindytech.iautoservice.utils;

import java.awt.*;

public class ColorLayout {

	//Background
	Color GRAY = new Color(89, 89, 89);
	Color DARK_GRAY = new Color(51, 51, 51);
	Color GOLD = new Color(240, 180, 5);
	Color headerColor = new Color(30, 39, 46);
	Color DARK_BLUE = new Color(0, 0, 128);

	
	public Color getGray() {
		return GRAY;
	}
	
	public Color getDarkGray() {
		return DARK_GRAY;
	}
	
	public Color getGold() {
		return GOLD;
	}
	
	public Color getBlueColor() {
		return Color.BLUE;
	}
	
	public Color getHeaderColor() {
		return headerColor;
	}
	
	public Color getDarkBlueColor() {
		return DARK_BLUE;
	}
	
	
	//PRINT BUTTON
	public Color getBackgroundPrintButtonColor() {
		return new Color(0, 61, 153);
	}
	public Color getForegroundPrintButtonColor() {
		return new Color(255, 255, 255);
	}


}
